-- --------------------------------------------------------
-- Книга "Трудно быть богом" 
-- Авторы: Аркадий и Борис Стругацкие
-- Команда группировки, так как авторов больше одного
-- --------------------------------------------------------

SELECT book.book_name AS 'Название книги'
FROM book
JOIN author_data ON book.book_id=author_data.book
WHERE author_data.book=12
GROUP BY book.book_name;